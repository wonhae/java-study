package chap04.ifelse;

public class Random {
	public static void main(String[] args) {
		//math라는 class에 random 이라는 method 가 있다. double return
		double r = Math.random();
		System.out.println(r);
		
		// 0~9
		r *= 10; // 10을 곱해서 넢으면 : 0.0000x  ~ 9.9999999x
		System.out.println(r);
		
		int i = (int) r;
		System.out.println(i);
		
		// 1 ~10
		i += 1;
		System.out.println(i
				
				
				
				);
		
		
		
	}
}
